def hitung_jumlah_range(start, end):
    total = (end - start + 1) * (start + end) / 2
    return total

start_range = float(input("Masukkan Angka Pertama: "))
end_range = float(input("Masukkan Angka Kedua: "))

hasil = hitung_jumlah_range(start_range, end_range)
